#include <iostream>
#include <fstream>
#include <string>
#include "random.h"

using namespace std;
 
int main (int argc, char *argv[]){

   Random rnd;
   int seed[4];
   int p1, p2;
   ifstream Primes("Primes");   //imposto = a p1 e p2 prendendo i primi 2 numeri dal file Primes
   if (Primes.is_open()){
      Primes >> p1 >> p2 ;
   } else cerr << "PROBLEM: Unable to open Primes" << endl;
   Primes.close();

   ifstream input("seed.in");  //carico in un vettore di 4 elementi i numeri 0000, 0000, 0000, 0001 e costruisco un oggetto di classe random
   string property;            //utilizzando i quattro numeri e p1 e p2
   if (input.is_open()){
      while ( !input.eof() ){
         input >> property;
         if( property == "RANDOMSEED" ){
            input >> seed[0] >> seed[1] >> seed[2] >> seed[3];
            rnd.SetRandom(seed,p1,p2);
         }
      }
      input.close();
   } else cerr << "PROBLEM: Unable to open seed.in" << endl;

   for(int i=0; i<20; i++){
      cout << rnd.Rannyu() << endl;  //genero 20 numeri casuali con la funzione Rannyu della classe random
   }

   rnd.SaveSeed();  //salvo i nuovi 4 seeds in un file esterno
   return 0;
}

